package it.polimi.ingsw.LM22.model;

public class DiceMalusEx {
	
	private final Integer period;
	private final Integer malusValue;
	
	public DiceMalusEx(Integer period, Integer malusValue){
		this.period = period;
		this.malusValue = malusValue;
	}

	public Integer getPeriod() {
		return period;
	}

	public Integer getMalusValue() {
		return malusValue;
	}
	
	
	
	
}
